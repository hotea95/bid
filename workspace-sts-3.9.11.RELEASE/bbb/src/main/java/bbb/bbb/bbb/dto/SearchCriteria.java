package bbb.bbb.bbb.dto;

public class SearchCriteria extends Criteria {

	private String searchType = "";
	private String keyword = "";
	private String searchTypeYear = "";
	private String keywordYear = "";
	private String searchTypeName = "";
	private String keywordName = "";
	private String searchTypeStat = "";
	private String keywordStat = "";

	public String getSearchTypeYear() {
		return searchTypeYear;
	}

	public void setSearchTypeYear(String searchTypeYear) {
		this.searchTypeYear = searchTypeYear;
	}

	public String getKeywordYear() {
		return keywordYear;
	}

	public void setKeywordYear(String keywordYear) {
		this.keywordYear = keywordYear;
	}

	public String getSearchTypeName() {
		return searchTypeName;
	}

	public void setSearchTypeName(String searchTypeName) {
		this.searchTypeName = searchTypeName;
	}

	public String getKeywordName() {
		return keywordName;
	}

	public void setKeywordName(String keywordName) {
		this.keywordName = keywordName;
	}

	public String getSearchTypeStat() {
		return searchTypeStat;
	}

	public void setSearchTypeStat(String searchTypeStat) {
		this.searchTypeStat = searchTypeStat;
	}

	public String getKeywordStat() {
		return keywordStat;
	}

	public void setKeywordStat(String keywordStat) {
		this.keywordStat = keywordStat;
	}

	public String getSearchType() {
		return searchType;
	}

	public void setSearchType(String searchType) {
		this.searchType = searchType;
	}

	public String getKeyword() {
		return keyword;
	}

	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	
	

}
