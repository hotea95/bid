package ccc.ccc.ccc.dto;


public class SearchCriteria extends Criteria{

   private String searchType ="";
   private String keyword = "";
   private String searchTypeMan ="";
   private String keywordMan = "";
   private String searchTypeYear ="";
   private String keywordYear = "";
   private String searchTypeAdd ="";
   private String keywordAdd = "";
public String getSearchType() {
	return searchType;
}
public void setSearchType(String searchType) {
	this.searchType = searchType;
}
public String getKeyword() {
	return keyword;
}
public void setKeyword(String keyword) {
	this.keyword = keyword;
}
public String getSearchTypeMan() {
	return searchTypeMan;
}
public void setSearchTypeMan(String searchTypeMan) {
	this.searchTypeMan = searchTypeMan;
}
public String getKeywordMan() {
	return keywordMan;
}
public void setKeywordMan(String keywordMan) {
	this.keywordMan = keywordMan;
}
public String getSearchTypeYear() {
	return searchTypeYear;
}
public void setSearchTypeYear(String searchTypeYear) {
	this.searchTypeYear = searchTypeYear;
}
public String getKeywordYear() {
	return keywordYear;
}
public void setKeywordYear(String keywordYear) {
	this.keywordYear = keywordYear;
}
public String getSearchTypeAdd() {
	return searchTypeAdd;
}
public void setSearchTypeAdd(String searchTypeAdd) {
	this.searchTypeAdd = searchTypeAdd;
}
public String getKeywordAdd() {
	return keywordAdd;
}
public void setKeywordAdd(String keywordAdd) {
	this.keywordAdd = keywordAdd;
}

}
